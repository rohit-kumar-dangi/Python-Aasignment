# Write a program to demonstrate different numeric data types (int, float) and type conversion.

# int
a = 10
print("Value of a:", a)
print("Type of a:", type(a))

# float
b = 5.5
print("\nValue of b:", b)
print("Type of b:", type(b))


# type conversion

# int to float
c = float(a)
print("Convert int to float:", c)
print("Type:", type(c))

# float to int
d = int(b)
print("Convert float to int:", d)
print("Type:", type(d))