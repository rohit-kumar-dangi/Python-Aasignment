# Write a program using built-in functions on list, string, and dictionary.

# List
lst = [1, 5, 3, 9]
print("Max:", max(lst))
print("Min:", min(lst))
print("Sum:", sum(lst))

# String
s = "python"
print("Length:", len(s))
print("Upper:", s.upper())

# Dictionary
d = {"a": 1, "b": 2}
print("Keys:", d.keys())
print("Values:", d.values())
print("Items:", d.items())